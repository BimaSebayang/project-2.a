package id.co.roxas.common.bean.auth;

public class CookiesAdvBean {
    private String cookiesValue;
    private String cookiesKey;
    private String cookiesAccessTime;
    private String cookiesUuid;
	public String getCookiesValue() {
		return cookiesValue;
	}
	public void setCookiesValue(String cookiesValue) {
		this.cookiesValue = cookiesValue;
	}
	public String getCookiesKey() {
		return cookiesKey;
	}
	public void setCookiesKey(String cookiesKey) {
		this.cookiesKey = cookiesKey;
	}
	public String getCookiesAccessTime() {
		return cookiesAccessTime;
	}
	public void setCookiesAccessTime(String cookiesAccessTime) {
		this.cookiesAccessTime = cookiesAccessTime;
	}
	public String getCookiesUuid() {
		return cookiesUuid;
	}
	public void setCookiesUuid(String cookiesUuid) {
		this.cookiesUuid = cookiesUuid;
	}
    
    
}

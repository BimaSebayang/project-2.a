package id.co.roxas.deep.learning.inteligence.findPathInMazeV2;

import java.util.List;

public class Path2D {
	private int x;
	private int y;

	public Path2D() {
	}

	

	public Path2D(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	

}
